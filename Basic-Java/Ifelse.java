public class Ifelse{
    public static void main(String args[]){
        int age =27;
        if(age>=4){
            System.out.println("Adult");
        }
        if(age>20){
            System.out.println("Teen");
        }
        else if(age>25){
            System.out.println("Teen4");

        }
        else{
            System.out.println("Not adult");
        }
    }
}